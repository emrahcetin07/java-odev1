public class loopDemo {
    public static void main(String[] args) {

        // for

        for (int i = 2; i < 10; i+=2) {
            System.out.println(i);
        }
        System.out.println("for döngüsü bitti");

        //While
        int i=1;
        while (i<10){
            System.out.println(i);
            i++;

        }
        System.out.println("While döngüsü bitti");
    }

}
